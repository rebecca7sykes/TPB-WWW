			A great lesson plan results in your students accomplishing their objectives, but where do you go to start creating that plan? <br/><br/>

			<b>Top 5 Tips For Teachers To Create A Great Lesson Plan:</b><br/><br/>

			<b>5. Talk to other teachers. </b><br/>
			Great ideas are often come from brainstorming, and discussing your ideas, and hearing the ideas of others is a great way to share techniques you've learned, and find out what else is out there. Teacher Plan Book allows you to communicate with teachers all across the United States, ensuring that you're up to date on the newest techniques and ideas in your subject.<br/><br/>

			<b>4. Keep it simple.</b><br/>
			Make sure that the materials utilized in your lesson plans fully relate to the learning activities described. Adding lots of unnecessary extras may end up distracting from the objectives, rather than helping to achieve them. <br/><br/>

			<b>3. Increase your efficiency.</b><br/>
			Every level of student has his or her own level of ability, and if you don't take this into account, you'll be an inefficient educator. With so much to teach in so little time, efficiency is key. <br/><br/>

			<b>2. Skip the busywork</b><br/>
			Students are aware when you've given them busy work, and so do parents and administrators. Ensure that activities assigned to your students contribute DIRECTLY to accomplishing the lesson objective, and don't be afraid to bounce ideas off of other teachers before implementing these ideas.  <br/><br/>

